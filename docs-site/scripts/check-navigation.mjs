import { existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { autogeneratedDirectories, pages, sidebarSections } from '../src/config/navigation.mjs';
import { locales as localeConfig, routedLanguageCodes } from '../src/config/locales.mjs';

const rootDir = dirname(fileURLToPath(import.meta.url));
const docsRoot = join(rootDir, '..', 'src', 'content', 'docs');
const locales = routedLanguageCodes.map((id) => ({
	id,
	lang: localeConfig[id]?.lang ?? id,
	pathPrefix: [id],
}));

const failures = [];
const links = new Map();

const routeSegments = (link) => {
	const cleanLink = link.replace(/^\/+|\/+$/g, '');
	return cleanLink ? cleanLink.split('/') : ['index'];
};

const documentPathFor = (link, locale) => {
	const segments = routeSegments(link);
	const name = segments.at(-1);
	const directories = name === 'index' ? [] : segments.slice(0, -1);
	const filename = name === 'index' ? 'index.md' : `${name}.md`;

	return join(docsRoot, ...locale.pathPrefix, ...directories, filename);
};

for (const [id, item] of Object.entries(pages)) {
	if (!item.link.startsWith('/') || !item.link.endsWith('/')) {
		failures.push(`${id}: link must start and end with "/": ${item.link}`);
	}

	const previous = links.get(item.link);
	if (previous) {
		failures.push(`${id}: duplicate link "${item.link}" already used by ${previous}`);
	}
	links.set(item.link, id);

	for (const locale of locales) {
		const expectedPath = documentPathFor(item.link, locale);
		if (!existsSync(expectedPath)) {
			failures.push(`${id}: missing ${locale.id} document for ${item.link}: ${expectedPath}`);
		}
	}
}

for (const directory of autogeneratedDirectories) {
	for (const locale of locales) {
		const expectedPath = join(docsRoot, ...locale.pathPrefix, directory);
		if (!existsSync(expectedPath)) {
			failures.push(`autogenerated "${directory}": missing ${locale.id} directory: ${expectedPath}`);
		}
	}
}

const validateSidebarTranslationKeys = (items, trail = []) => {
	for (const item of items) {
		const label = item.label ?? item.link ?? item.autogenerate?.directory ?? '<unknown>';
		const nextTrail = [...trail, label];

		for (const locale of locales) {
			if (
				item.translations?.[locale.id] &&
				locale.lang !== locale.id &&
				!item.translations[locale.lang]
			) {
				failures.push(
					`${nextTrail.join(' > ')}: translation for ${locale.id} must also define "${locale.lang}"`
				);
			}
		}

		if (item.items) {
			validateSidebarTranslationKeys(item.items, nextTrail);
		}
	}
};

validateSidebarTranslationKeys(sidebarSections);

if (failures.length > 0) {
	console.error(`navigation check failed (${failures.length})`);
	for (const failure of failures) {
		console.error(`- ${failure}`);
	}
	process.exitCode = 1;
} else {
	console.log(
		`navigation ok (${Object.keys(pages).length} linked pages, ${autogeneratedDirectories.length} autogenerated directory)`
	);
}
